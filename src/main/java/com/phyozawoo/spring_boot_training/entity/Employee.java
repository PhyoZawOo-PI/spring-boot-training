package com.phyozawoo.spring_boot_training.entity;

import com.amazonaws.services.dynamodbv2.datamodeling.*;
import io.swagger.v3.oas.annotations.media.Schema;
import lombok.AllArgsConstructor;
import lombok.NoArgsConstructor;

@AllArgsConstructor
@NoArgsConstructor
@DynamoDBTable(tableName = "employee")
public class Employee {
    @Schema(
            example = "aheaw89yr9q3i20iq0eq"
    )
    @DynamoDBHashKey(attributeName = "employee_id")
    @DynamoDBAutoGeneratedKey
    private String employeeId;
    @Schema(
            example = "Phyo Zaw"
    )
    @DynamoDBAttribute(attributeName = "first_name")
    private String firstName;
    @Schema(
            example = "Oo"
    )
    @DynamoDBAttribute(attributeName = "last_name")
    private String lastName;
    @Schema(
            example = "phyozawoo@gmail.com"
    )
    @DynamoDBIndexHashKey(globalSecondaryIndexName = "email_index")
    private String email;
    @DynamoDBAttribute
    private Department department;

    public String getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(String employeeId) {
        this.employeeId = employeeId;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Department getDepartment() {
        return department;
    }

    public void setDepartment(Department department) {
        this.department = department;
    }
}
